    <p class="index2"><b>（一）未取得机动车维修经营许可，非法从事机动车维修经营的；</b></p>
    <p class="index2"><b>（二）使用无效、伪造、变造机动车维修经营许可证件，非法从事机动车维修经营的； </b></p>
    <p class="index2"><b>（三）超越许可事项，非法从事机动车维修经营的。</b></p>
