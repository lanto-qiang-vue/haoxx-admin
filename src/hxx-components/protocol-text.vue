    <p class="index2"><b>（四）有健全的维修管理制度。包括质量管理制度、安全生产管理制度、车辆维修档案管理制度、人员培训制度、设备管理制度及配件管理制度。具体要求按照国家标准《汽车维修业开业条件》（GB/T16739）相关条款的规定执行。</b></p>
    <p class="index2"><b>（五）有必要的环境保护措施。具体要求按照国家标准《汽车维修业开业条件》（GB/T16739）相关条款的规定执行。</b></p>
    <p class="index2"><b>第十二条从事危险货物运输车辆维修的汽车维修经营者，除具备汽车维修经营一类维修经营业务的开业条件外，还应当具备下列条件：</b></p>
    <p class="index2"><b>（一）有与其作业内容相适应的专用维修车间和设备、设施，并设置明显的指示性标志；</b></p>
    <p class="index2"><b>（二）有完善的突发事件应急预案，应急预案包括报告程序、应急指挥以及处置措施等内容；</b></p>
    <p class="index2"><b>（三）有相应的安全管理人员；</b></p>
    <p class="index2"><b>（四）有齐全的安全操作规程。</b></p>
    <p class="index2"><b>本规定所称危险货物运输车辆维修，是指对运输易燃、易爆、腐蚀、放射性、剧毒等性质货物的机动车维修，不包含对危险货物运输车辆罐体的维修。 </b></p>
    <p class="index2"><b>第十三条申请从事摩托车维修经营的，应当符合下列条件：</b></p>
    <p class="index2"><b>（一）有与其经营业务相适应的摩托车维修停车场和生产厂房。租用的场地应有书面的租赁合同，且租赁期限不得少于1年。停车场和生产厂房的面积按照国家标准《摩托车维修业开业条件》（GB/T18189）相关条款的规定执行。</b></p>
    <p class="index2"><b>（二）有与其经营业务相适应的设备、设施。所配备的计量设备应符合国家有关技术标准要求，并经法定检定机构检定合格。具体要求按照国家标准《摩托车维修业开业条件》（GB/T18189）相关条款的规定执行。</b></p>
    <p class="index2"><b>（三）有必要的技术人员：</b></p>
    <p class="index2"><b>1.从事一类维修业务的应当至少有1名质量检验人员。质量检验人员应当熟悉各类摩托车维修检测作业规范，掌握摩托车维修故障诊断和质量检验的相关技术，熟悉摩托车维修服务收费标准及相关政策法规和技术规范。</b></p>
    <p class="index2"><b>2.按照其经营业务分别配备相应的机修、电器、钣金、涂漆的维修技术人员。机修、电器、钣金、涂漆的维修技术人员应当熟悉所从事工种的维修技术和操作规范，并了解摩托车维修及相关政策法规。</b></p>
    <p class="index2"><b>（四）有健全的维修管理制度。包括质量管理制度、安全生产管理制度、摩托车维修档案管理制度、人员培训制度、设备管理制度及配件管理制度。具体要求按照国家标准《摩托车维修业开业条件》（GB/T18189）相关条款的规定执行。</b></p>
    <p class="index2"><b>（五）有必要的环境保护措施。具体要求按照国家标准《摩托车维修业开业条件》（GB/T18189）相关条款的规定执行。</b></p>
    <p class="index2"><b>第十四条申请从事机动车维修经营的，应当向所在地的县级道路运输管理机构提出申请，并提交下列材料：</b></p>
    <p class="index2"><b>（一）《交通行政许可申请书》、有关维修经营申请者的营业执照原件和复印件；</b></p>
    <p class="index2"><b>（二）经营场地（含生产厂房和业务接待室）、停车场面积材料、土地使用权及产权证明原件和复印件；</b></p>
    <p class="index2"><b>（三）技术人员汇总表，以及各相关人员的学历、技术职称或职业资格证明等文件原件和复印件；</b></p>
    <p class="index2"><b>（四）维修检测设备及计量设备检定合格证明原件和复印件；</b></p>
    <p class="index2"><b>（五）按照汽车、其他机动车、危险货物运输车辆、摩托车维修经营，分别提供本规定第十一条、第十二条、第十三条规定条件的其他相关材料。</b></p>
    <p class="index2"><b>第十五条道路运输管理机构应当按照《中华人民共和国道路运输条例》和《交通行政许可实施程序规定》规范的程序实施机动车维修经营的行政许可。 </b></p>
    <p class="index2"><b>第十六条道路运输管理机构对机动车维修经营申请予以受理的，应当自受理申请之日起15日内作出许可或者不予许可的决定。符合法定条件的，道路运输管理机构作出准予行政许可的决定，向申请人出具《交通行政许可决定书》，在10日内向被许可人颁发机动车维修经营许可证件，明确许可事项；不符合法定条件的，道路运输管理机构作出不予许可的决定，向申请人出具《不予交通行政许可决定书》，说明理由，并告知申请人享有依法申请行政复议或者提起行政诉讼的权利。</b></p>
    <p class="index2"><b>机动车维修经营者应当在取得相应工商登记执照后，向道路运输管理机构申请办理机动车维修经营许可手续。</b></p>
    <p class="index2"><b>第十七条申请机动车维修连锁经营服务网点的，可由机动车维修连锁经营企业总部向连锁经营服务网点所在地县级道路运输管理机构提出申请，提交下列材料，并对材料真实性承担相应的法律责任：</b></p>
    <p class="index2"><b>（一）机动车维修连锁经营企业总部机动车维修经营许可证件复印件；</b></p>
    <p class="index2"><b>（二）连锁经营协议书副本；</b></p>
    <p class="index2"><b>（三）连锁经营的作业标准和管理手册；</b></p>
    <p class="index2"><b>（四）连锁经营服务网点符合机动车维修经营相应开业条件的承诺书。</b></p>
    <p class="index2"><b>道路运输管理机构在查验申请资料齐全有效后，应当场或在5日内予以许可，并发给相应许可证件。连锁经营服务网点的经营许可项目应当在机动车维修连锁经营企业总部许可项目的范围内。</b></p>
    <p class="index2"><b>第十八条机动车维修经营许可证件实行有效期制。从事一、二类汽车维修业务和一类摩托车维修业务的证件有效期为6年；从事三类汽车维修业务、二类摩托车维修业务及其他机动车维修业务的证件有效期为3年。</b></p>
    <p class="index2"><b>机动车维修经营许可证件由各省、自治区、直辖市道路运输管理机构统一印制并编号，县级道路运输管理机构按照规定发放和管理。</b></p>
    <p class="index2"><b>第十九条机动车维修经营者应当在许可证件有效期届满前30日到作出原许可决定的道路运输管理机构办理换证手续。</b></p>
    <p class="index2"><b>第二十条机动车维修经营者变更经营资质、经营范围、经营地址、有效期限等许可事项的，应当向作出原许可决定的道路运输管理机构提出申请；符合本章规定许可条件、标准的，道路运输管理机构依法办理变更手续。</b></p>
    <p class="index2"><b>机动车维修经营者变更名称、法定代表人等事项的，应当向作出原许可决定的道路运输管理机构备案。</b></p>
    <p class="index2"><b>机动车维修经营者需要终止经营的，应当在终止经营前30日告知作出原许可决定的道路运输管理机构办理注销手续。</b></p>
    <p style="text-align: center;padding-top:15px;padding-bottom:15px;"><strong><span style="font-family: 宋体,SimSun; font-size: 16px;">第三章　维修经营</span></strong><span style="font-size: 12px;">&nbsp;</span></p>
    <p class="index2"><b>第二十一条机动车维修经营者应当按照经批准的行政许可事项开展维修服务。</b></p>
    <p class="index2"><b>第二十二条机动车维修经营者应当将机动车维修经营许可证件和《机动车维修标志牌》（见附件1）悬挂在经营场所的醒目位置。</b></p>
    <p class="index2"><b>《机动车维修标志牌》由机动车维修经营者按照统一式样和要求自行制作。</b></p>
    <p class="index2"><b>第二十三条机动车维修经营者不得擅自改装机动车，不得承修已报废的机动车，不得利用配件拼装机动车。</b></p>
    <p class="index2"><b>托修方要改变机动车车身颜色，更换发动机、车身和车架的，应当按照有关法律、法规的规定办理相关手续，机动车维修经营者在查看相关手续后方可承修。</b></p>
    <p class="index2"><b>第二十四条机动车维修经营者应当加强对从业人员的安全教育和职业道德教育，确保安全生产。</b></p>
    <p class="index2"><b>机动车维修从业人员应当执行机动车维修安全生产操作规程，不得违章作业。</b></p>
    <p class="index2"><b>第二十五条机动车维修产生的废弃物，应当按照国家的有关规定进行处理。</b></p>
    <p class="index2"><b>第二十六条机动车维修经营者应当公布机动车维修工时定额和收费标准，合理收取费用。 </b></p>
    <p class="index2"><b>机动车维修工时定额可按各省机动车维修协会等行业中介组织统一制定的标准执行，也可按机动车维修经营者报所在地道路运输管理机构备案后的标准执行，也可按机动车生产厂家公布的标准执行。当上述标准不一致时，优先适用机动车维修经营者备案的标准。</b></p>
    <p class="index2"><b>机动车维修经营者应当将其执行的机动车维修工时单价标准报所在地道路运输管理机构备案。</b></p>
    <p class="index2"><b>机动车生产厂家在新车型投放市场后六个月内，有义务向社会公布其维修技术信息和工时定额。具体要求按照国家有关部门关于汽车维修技术信息公开的规定执行。</b></p>
    <p class="index2"><b>第二十七条机动车维修经营者应当使用规定的结算票据，并向托修方交付维修结算清单。维修结算清单中，工时费与材料费应当分项计算。维修结算清单标准规范格式由交通运输部制定。</b></p>
    <p class="index2"><b>机动车维修经营者不出具规定的结算票据和结算清单的，托修方有权拒绝支付费用。</b></p>
    <p class="index2"><b>第二十八条机动车维修经营者应当按照规定，向道路运输管理机构报送统计资料。</b></p>
    <p class="index2"><b>道路运输管理机构应当为机动车维修经营者保守商业秘密。</b></p>
    <p class="index2"><b>第二十九条机动车维修连锁经营企业总部应当按照统一采购、统一配送、统一标识、统一经营方针、统一服务规范和价格的要求，建立连锁经营的作业标准和管理手册，加强对连锁经营服务网点经营行为的监管和约束，杜绝不规范的商业行为。</b></p>
    <p style="text-align: center;padding-top:15px;padding-bottom:15px;"><strong><span style="font-family: 宋体,SimSun; font-size: 16px;">第四章　质量管理</span></strong><span style="font-size: 12px;">&nbsp;</span></p>
    <p class="index2"><b>第三十条机动车维修经营者应当按照国家、行业或者地方的维修标准和规范进行维修。尚无标准或规范的，可参照机动车生产企业提供的维修手册、使用说明书和有关技术资料进行维修。</b></p>
    <p class="index2"><b>第三十一条机动车维修经营者不得使用假冒伪劣配件维修机动车。 </b></p>
    <p class="index2"><b>机动车维修配件实行追溯制度。机动车维修经营者应当记录配件采购、使用信息，查验产品合格证等相关证明，并按规定留存配件来源凭证。</b></p>
    <p class="index2"><b>托修方、维修经营者可以使用同质配件维修机动车。同质配件是指，产品质量等同或者高于装车零部件标准要求，且具有良好装车性能的配件。</b></p>
    <p class="index2"><b>机动车维修经营者对于换下的配件、总成，应当交托修方自行处理。</b></p>
    <p class="index2"><b>机动车维修经营者应当将原厂配件、同质配件和修复配件分别标识，明码标价，供用户选择。 </b></p>
    <p class="index2"><b>第三十二条机动车维修经营者对机动车进行二级维护、总成修理、整车修理的，应当实行维修前诊断检验、维修过程检验和竣工质量检验制度。</b></p>
    <p class="index2"><b>承担机动车维修竣工质量检验的机动车维修企业或机动车综合性能检测机构应当使用符合有关标准并在检定有效期内的设备，按照有关标准进行检测，如实提供检测结果证明，并对检测结果承担法律责任。</b></p>
    <p class="index2"><b>第三十三条机动车维修竣工质量检验合格的，维修质量检验人员应当签发《机动车维修竣工出厂合格证》（见附件2）；未签发机动车维修竣工出厂合格证的机动车，不得交付使用，车主可以拒绝交费或接车。 </b></p>
    <p class="index2"><b>第三十四条机动车维修经营者应当建立机动车维修档案，并实行档案电子化管理。维修档案应当包括：维修合同（托修单）、维修项目、维修人员及维修结算清单等。对机动车进行二级维护、总成修理、整车修理的，维修档案还应当包括：质量检验单、质量检验人员、竣工出厂合格证（副本）等。 </b></p>
    <p class="index2"><b>机动车维修经营者应当按照规定如实填报、及时上传承修机动车的维修电子数据记录至国家有关汽车电子健康档案系统。机动车生产厂家或者第三方开发、提供机动车维修服务管理系统的，应当向汽车电子健康档案系统开放相应数据接口。</b></p>
    <p class="index2"><b>机动车托修方有权查阅机动车维修档案。</b></p>
    <p class="index2"><b>第三十五条道路运输管理机构应当加强机动车维修从业人员管理，建立健全从业人员信用档案，加强从业人员诚信监管。</b></p>
    <p class="index2"><b>机动车维修经营者应当加强从业人员从业行为管理，促进从业人员诚信、规范从业维修。 </b></p>
    <p class="index2"><b>第三十六条道路运输管理机构应当加强对机动车维修经营的质量监督和管理，采用定期检查、随机抽样检测检验的方法，对机动车维修经营者维修质量进行监督。</b></p>
    <p class="index2"><b>道路运输管理机构可以委托具有法定资格的机动车维修质量监督检验单位，对机动车维修质量进行监督检验。</b></p>
    <p class="index2"><b>第三十七条机动车维修实行竣工出厂质量保证期制度。</b></p>
    <p class="index2"><b>汽车和危险货物运输车辆整车修理或总成修理质量保证期为车辆行驶20000公里或者100日；二级维护质量保证期为车辆行驶5000公里或者30日；一级维护、小修及专项修理质量保证期为车辆行驶2000公里或者10日。</b></p>
    <p class="index2"><b>摩托车整车修理或者总成修理质量保证期为摩托车行驶7000公里或者80日；维护、小修及专项修理质量保证期为摩托车行驶800公里或者10日。</b></p>
    <p class="index2"><b>其他机动车整车修理或者总成修理质量保证期为机动车行驶6000公里或者60日；维护、小修及专项修理质量保证期为机动车行驶700公里或者7日。</b></p>
    <p class="index2"><b>质量保证期中行驶里程和日期指标，以先达到者为准。</b></p>
    <p class="index2"><b>机动车维修质量保证期，从维修竣工出厂之日起计算。</b></p>
    <p class="index2"><b>第三十八条在质量保证期和承诺的质量保证期内，因维修质量原因造成机动车无法正常使用，且承修方在3日内不能或者无法提供因非维修原因而造成机动车无法使用的相关证据的，机动车维修经营者应当及时无偿返修，不得故意拖延或者无理拒绝。</b></p>
    <p class="index2"><b>在质量保证期内，机动车因同一故障或维修项目经两次修理仍不能正常使用的，机动车维修经营者应当负责联系其他机动车维修经营者，并承担相应修理费用。</b></p>
    <p class="index2"><b>第三十九条机动车维修经营者应当公示承诺的机动车维修质量保证期。所承诺的质量保证期不得低于第三十七条的规定。</b></p>
    <p class="index2"><b>第四十条道路运输管理机构应当受理机动车维修质量投诉，积极按照维修合同约定和相关规定调解维修质量纠纷。</b></p>
    <p class="index2"><b>第四十一条机动车维修质量纠纷双方当事人均有保护当事车辆原始状态的义务。必要时可拆检车辆有关部位，但双方当事人应同时在场，共同认可拆检情况。</b></p>
    <p class="index2"><b>第四十二条对机动车维修质量的责任认定需要进行技术分析和鉴定，且承修方和托修方共同要求道路运输管理机构出面协调的，道路运输管理机构应当组织专家组或委托具有法定检测资格的检测机构作出技术分析和鉴定。鉴定费用由责任方承担。</b></p>
    <p class="index2"><b>第四十三条对机动车维修经营者实行质量信誉考核制度。机动车维修质量信誉考核办法另行制定。</b></p>
    <p class="index2"><b>机动车维修质量信誉考核内容应当包括经营者基本情况、经营业绩（含奖励情况）、不良记录等</b></p>
    <p class="index2"><b>第四十四条道路运输管理机构应当建立机动车维修企业诚信档案。机动车维修质量信誉考核结果是机动车维修诚信档案的重要组成部分。</b></p>
    <p class="index2"><b>道路运输管理机构建立的机动车维修企业诚信信息，除涉及国家秘密、商业秘密外，应当依法公开，供公众查阅。</b></p>
    <p style="text-align: center;padding-top:15px;padding-bottom:15px;"><strong><span style="font-family: 宋体,SimSun; font-size: 16px;">第五章　监督检查</span></strong><span style="font-size: 12px;">&nbsp;</span></p>
    <p class="index2"><b>第四十五条道路运输管理机构应当加强对机动车维修经营活动的监督检查。</b></p>
    <p class="index2"><b>道路运输管理机构应当依法履行对维修经营者所取得维修经营许可的监管职责，定期核对许可登记事项和许可条件。对许可登记内容发生变化的，应当依法及时变更；对不符合法定条件的，应当责令限期改正。</b></p>
    <p class="index2"><b>道路运输管理机构的工作人员应当严格按照职责权限和程序进行监督检查，不得滥用职权、徇私舞弊，不得乱收费、乱罚款。</b></p>
    <p class="index2"><b>第四十六条道路运输管理机构应当积极运用信息化技术手段，科学、高效地开展机动车维修管理工作。</b></p>
    <p class="index2"><b>第四十七条道路运输管理机构的执法人员在机动车维修经营场所实施监督检查时，应当有2名以上人员参加，并向当事人出示交通运输部监制的交通行政执法证件。</b></p>
    <p class="index2"><b>道路运输管理机构实施监督检查时，可以采取下列措施：</b></p>
    <p class="index2"><b>（一）询问当事人或者有关人员，并要求其提供有关资料；</b></p>
    <p class="index2"><b>（二）查询、复制与违法行为有关的维修台帐、票据、凭证、文件及其他资料，核对与违法行为有关的技术资料；</b></p>
    <p class="index2"><b>（三）在违法行为发现场所进行摄影、摄像取证；</b></p>
    <p class="index2"><b>（四）检查与违法行为有关的维修设备及相关机具的有关情况。</b></p>
    <p class="index2"><b>检查的情况和处理结果应当记录，并按照规定归档。当事人有权查阅监督检查记录。</b></p>
    <p class="index2"><b>第四十八条从事机动车维修经营活动的单位和个人，应当自觉接受道路运输管理机构及其工作人员的检查，如实反映情况，提供有关资料。</b></p>
